#Revisão listas
list = [1, 2 , 3, 4, 5]

#Acessando o primeiro item da lista
print(list[0])

#Acessar o último item da lista
print(list[4])

#Adicionando número no final
list.append(6)

print(list)

#Remover o segundo elemento
list.remove(list[1])

print(list)

#Ordenar a lista em ordem decrescente
list.sort(reverse=True)

print(list)
